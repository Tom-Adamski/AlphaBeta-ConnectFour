package alphabeta.connectfour;

public class Coup {
	
	private int val;
	private int noColonne;
	public Coup(int val, int noColonne) {
		super();
		this.val = val;
		this.noColonne = noColonne;
	}
	public int getVal() {
		return val;
	}
	public int getNoColonne() {
		return noColonne;
	}
	
	
	
	

}
